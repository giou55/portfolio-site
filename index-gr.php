<script>
    $(document).ready(function(){
      $('#submit-btn').click(function(event){
        event.preventDefault();
         $.ajax({
            dataType: 'JSON',
            url: 'sendmail-gr.php',
            type: 'POST',
            data: $('#contact').serialize(),
            beforeSend: function(xhr){
              $('#submit-btn').html('ΓΙΝΕΤΑΙ ΑΠΟΣΤΟΛΗ...');
            },
            success: function(response){
              if(response){
                console.log(response);
                if(response['signal'] == 'ok'){
                  $('#msg').html('<div class="alert alert-success">'+ response['msg']  +'</div>');
                  $('input, textarea').val(function() {
                     return this.defaultValue;
                  });
                }
                else{
                  $('#msg').html('<div class="alert alert-danger">'+ response['msg'] +'</div>');
                }
              }
            },
            error: function(){
              $('#msg').html('<div class="alert alert-danger">Συνέβει κάποιο σφάλμα. Παρακαλώ προσπαθήστε πάλι αργότερα.</div>');
            },
            complete: function(){
              $('#submit-btn').html('ΑΠΟΣΤΟΛΗ');
            }
          });
      });
    });
</script>


